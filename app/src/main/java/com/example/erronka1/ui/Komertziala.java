package com.example.erronka1.ui;

public class Komertziala {
    private String name;
    private String email;


    public Komertziala(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }


}

